/* flags.c -- 演示一些格式标记 */
#include <stdio.h>
int main(void) {
	printf("%x %X %#x\n", 31, 31, 31);
	printf("**%d**% d**% d**\n", 42, 42, -42);//空格：正数前面加个空格，负数前面不加。
	printf("**%5d**%5.3d**%05d**%05.3d**\n", 6, 6, 6, 6);

	return 0;
}

//输出结果：
//1f 1F 0x1f
//**42** 42**-42**
//**    6**  006**00006**  006**